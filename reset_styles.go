package styx

func (s *Styx) ResetBrowserStyles() {
	{
		s.Build("*,::before,::after").
			M("0").
			P("0").
			BoxBorder().
			Finish()
	}
	{
		s.Build("html,body").
			H("100%").
			LineHeight("1.5").
			FontFamily("system-ui").
			Antialiased().
			Finish()
	}
	{
		s.Build("body,h1,h2,h3,h4,p,figure,blockquote,dl,dd").
			M("0").
			Finish()
	}
	{
		s.Build("ul,ol").
			ListStyle("none").
			M("0").
			P("0").
			Finish()
	}
	{
		s.Build("a").
			NoUnderline().
			TextColor(ColorInherit).
			Finish()
	}
	{
		s.Build("img,video,svg,canvas").
			Block().
			MaxW("100%").
			Finish()
	}
	{
		s.Build("button,input,select,textarea").
			Font("inherit").
			TextColor(ColorInherit).
			Border("none").
			M("0").
			P("0").
			Finish()
	}
	{
		s.Build("button").
			Cursor("pointer").
			Finish()
	}
	{
		s.Build("table").
			BorderCollapse().
			BorderSpacing("0").
			Finish()
	}
}
