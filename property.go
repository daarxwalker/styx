func WithProp(key string, values ...string) Node {
	return func(n *node) {
		n.nodes = append(
			n.nodes, &node{
				nodeType: nodeTypeProperty,
				key:      key,
				value:    strings.Join(values, " "),
			},
		)
	}
}

